def ft_add(num1: int, num2: int) -> int:
    '''Simple addition function'''
    return (num1 + num2)


def ft_subtract(num1: int, num2: int) -> int:
    '''Simple subtraction function'''
    return (num1 - num2)


def ft_mult(num1: int, num2: int) -> int:
    '''Simple multiplication function'''
    return (num1 * num2)


def ft_div(num1: int, num2: int) -> int:
    '''Simple division function'''
    return (int(num1 / num2))
